// Módulo (%)
// Operador binário. Retorna o inteiro restante da divisão dos dois operandos.

12 % 5; // retorna 2

// incremento (++) e Decremento (--)
++x;
x++;

const a = ++2; // 3 - recebe antes da execução
const b = 2++; // 2 - recebe após a execução

--x;
x--;
// Negação (-) e adição (+)
-3; // -(3)
+'3'; // retorna 3

// true tem valor 1
// false tem valor 0
// ao adicionar o operador o boolear é convertido para numérico
+true; // retorna 1
+false; // retorna 0
-true; // retorna -1

// Operador de exponenciação (**)
2 ** 3; // retorna 8
10 ** -1; // retorna 0.1

// Operador de agrupamento ()
2 * (3 + 2);
